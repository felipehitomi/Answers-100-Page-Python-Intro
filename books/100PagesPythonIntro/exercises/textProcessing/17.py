fruit = 'apple'

print(f'{fruit:=>10}')
print('{:=>10}'.format(fruit))
print('-----------------------------')
print(f'{fruit:=<10}')
print('{:=<10}'.format(fruit))
print('-----------------------------')
print(f'{fruit:=^10}')
print('{:=^10}'.format(fruit))
print('-----------------------------')
print(f'{fruit:^10}')
print('{:^10}'.format(fruit))
